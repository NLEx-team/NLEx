-- ============================================================
-- HOSPITAL DATABASE - INSERT DOCTORS (800 строк)
-- ============================================================

SET DEFINE OFF;

DECLARE
    TYPE t_last   IS TABLE OF VARCHAR2(50);
    TYPE t_first  IS TABLE OF VARCHAR2(50);
    TYPE t_mid    IS TABLE OF VARCHAR2(50);
    TYPE t_spec   IS TABLE OF VARCHAR2(100);
    TYPE t_dept   IS TABLE OF VARCHAR2(100);
    TYPE t_cat    IS TABLE OF VARCHAR2(30);

    v_last   t_last  := t_last('Иванов','Петров','Сидоров','Козлов','Новиков','Морозов','Волков','Соколов',
                                'Лебедев','Попов','Кузнецов','Смирнов','Михайлов','Федоров','Орлов',
                                'Николаев','Захаров','Степанов','Павлов','Семёнов','Голубев','Виноградов',
                                'Богданов','Воробьёв','Фролов','Александров','Медведев','Васильев','Гусев',
                                'Зайцев','Абрамов','Тихонов','Громов','Беляев','Антонов','Давыдов','Ильин',
                                'Макаров','Соловьёв','Романов','Осипов','Яковлев','Кириллов','Белоусов');

    v_first_m t_first := t_first('Александр','Дмитрий','Сергей','Андрей','Алексей','Максим','Иван','Михаил',
                                  'Николай','Павел','Артём','Владимир','Евгений','Константин','Роман',
                                  'Денис','Игорь','Антон','Виктор','Илья');

    v_first_f t_first := t_first('Анна','Мария','Елена','Ольга','Наталья','Татьяна','Ирина','Светлана',
                                  'Екатерина','Юлия','Людмила','Галина','Вера','Надежда','Алина',
                                  'Валентина','Оксана','Наталия','Марина','Виктория');

    v_mid_m   t_mid  := t_mid('Александрович','Дмитриевич','Сергеевич','Андреевич','Алексеевич',
                               'Максимович','Иванович','Михайлович','Николаевич','Павлович',
                               'Артёмович','Владимирович','Евгеньевич','Константинович','Романович');

    v_mid_f   t_mid  := t_mid('Александровна','Дмитриевна','Сергеевна','Андреевна','Алексеевна',
                               'Максимовна','Ивановна','Михайловна','Николаевна','Павловна',
                               'Артёмовна','Владимировна','Евгеньевна','Константиновна','Романовна');

    v_spec    t_spec := t_spec('Терапевт','Хирург','Кардиолог','Невролог','Педиатр',
                                'Офтальмолог','Отоларинголог','Ортопед','Гастроэнтеролог','Эндокринолог',
                                'Дерматолог','Уролог','Гинеколог','Пульмонолог','Ревматолог',
                                'Онколог','Психиатр','Нефролог','Аллерголог','Инфекционист');

    v_dept    t_dept := t_dept('Терапевтическое отделение','Хирургическое отделение','Кардиологическое отделение',
                                'Неврологическое отделение','Педиатрическое отделение','Офтальмологическое отделение',
                                'ЛОР-отделение','Ортопедическое отделение','Гастроэнтерологическое отделение',
                                'Эндокринологическое отделение','Дерматологическое отделение','Урологическое отделение',
                                'Гинекологическое отделение','Пульмонологическое отделение','Онкологическое отделение');

    v_cat     t_cat  := t_cat('Первая','Вторая','Высшая','Без категории');

    v_last_n   VARCHAR2(50);
    v_first_n  VARCHAR2(50);
    v_mid_n    VARCHAR2(50);
    v_spec_n   VARCHAR2(100);
    v_dept_n   VARCHAR2(100);
    v_exp      NUMBER;
    v_hire     DATE;
    v_salary   NUMBER;
    v_spec_idx NUMBER;

BEGIN
    FOR i IN 1..800 LOOP
        IF MOD(i, 2) = 0 THEN
            v_last_n  := v_last(MOD(i-1, v_last.COUNT) + 1);
            v_first_n := v_first_m(MOD(i-1, v_first_m.COUNT) + 1);
            v_mid_n   := v_mid_m(MOD(i-1, v_mid_m.COUNT) + 1);
        ELSE
            -- Женские фамилии с окончанием -а
            v_last_n  := v_last(MOD(i-1, v_last.COUNT) + 1) || 'а';
            v_first_n := v_first_f(MOD(i-1, v_first_f.COUNT) + 1);
            v_mid_n   := v_mid_f(MOD(i-1, v_mid_f.COUNT) + 1);
        END IF;

        v_spec_idx := MOD(i-1, v_spec.COUNT) + 1;
        v_spec_n   := v_spec(v_spec_idx);
        v_dept_n   := v_dept(MOD(v_spec_idx-1, v_dept.COUNT) + 1);
        v_exp      := MOD(i, 35) + 1;
        v_hire     := SYSDATE - (v_exp * 365) - MOD(i * 7, 180);
        v_salary   := 45000 + MOD(i * 1337, 85000);

        INSERT INTO DOCTORS (
            DOCTOR_ID, LAST_NAME, FIRST_NAME, MIDDLE_NAME,
            SPECIALIZATION, DEPARTMENT, EXPERIENCE_YEARS,
            HIRE_DATE, SALARY, PHONE, EMAIL, CATEGORY, IS_ACTIVE
        ) VALUES (
            SEQ_DOCTORS.NEXTVAL,
            v_last_n,
            v_first_n,
            v_mid_n,
            v_spec_n,
            v_dept_n,
            v_exp,
            v_hire,
            v_salary,
            '+7-9' || TO_CHAR(MOD(i * 31, 90) + 10) || '-' || TO_CHAR(MOD(i * 73, 900) + 100) || '-' || TO_CHAR(MOD(i * 137, 90) + 10) || '-' || TO_CHAR(MOD(i * 17, 90) + 10),
            LOWER(v_first_n) || '.' || LOWER(v_last_n) || MOD(i, 100) || '@hospital.ru',
            v_cat(MOD(i-1, v_cat.COUNT) + 1),
            CASE WHEN MOD(i, 20) = 0 THEN 'N' ELSE 'Y' END
        );

        IF MOD(i, 100) = 0 THEN
            COMMIT;
        END IF;
    END LOOP;

    COMMIT;
    DBMS_OUTPUT.PUT_LINE('Врачи вставлены: 800 строк');
END;
