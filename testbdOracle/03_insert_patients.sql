DECLARE
    TYPE t_last   IS TABLE OF VARCHAR2(50);
    TYPE t_first  IS TABLE OF VARCHAR2(50);
    TYPE t_mid    IS TABLE OF VARCHAR2(50);
    TYPE t_city   IS TABLE OF VARCHAR2(50);
    TYPE t_blood  IS TABLE OF VARCHAR2(5);
    TYPE t_diag   IS TABLE OF VARCHAR2(200);

    v_last   t_last := t_last(
        'Иванов','Петров','Сидоров','Козлов','Новиков','Морозов','Волков','Соколов',
        'Лебедев','Попов','Кузнецов','Смирнов','Михайлов','Федоров','Орлов',
        'Николаев','Захаров','Степанов','Павлов','Семёнов','Голубев','Виноградов',
        'Богданов','Воробьёв','Фролов','Александров','Медведев','Васильев','Гусев',
        'Зайцев','Абрамов','Тихонов','Громов','Беляев','Антонов','Давыдов','Ильин',
        'Макаров','Соловьёв','Романов','Осипов','Яковлев','Кириллов','Белоусов',
        'Зимин','Крылов','Борисов','Куликов','Некрасов','Тарасов'
    );

    v_first_m t_first := t_first(
        'Александр','Дмитрий','Сергей','Андрей','Алексей','Максим','Иван','Михаил',
        'Николай','Павел','Артём','Владимир','Евгений','Константин','Роман',
        'Денис','Игорь','Антон','Виктор','Илья','Олег','Кирилл','Тимур','Глеб','Руслан'
    );

    v_first_f t_first := t_first(
        'Анна','Мария','Елена','Ольга','Наталья','Татьяна','Ирина','Светлана',
        'Екатерина','Юлия','Людмила','Галина','Вера','Надежда','Алина',
        'Валентина','Оксана','Наталия','Марина','Виктория','Дарья','Полина','Ксения','Зоя','Лариса'
    );

    v_mid_m t_mid := t_mid(
        'Александрович','Дмитриевич','Сергеевич','Андреевич','Алексеевич',
        'Максимович','Иванович','Михайлович','Николаевич','Павлович',
        'Артёмович','Владимирович','Евгеньевич','Константинович','Романович',
        'Денисович','Игоревич','Антонович','Викторович','Ильич'
    );

    v_mid_f t_mid := t_mid(
        'Александровна','Дмитриевна','Сергеевна','Андреевна','Алексеевна',
        'Максимовна','Ивановна','Михайловна','Николаевна','Павловна',
        'Артёмовна','Владимировна','Евгеньевна','Константиновна','Романовна',
        'Денисовна','Игоревна','Антоновна','Викторовна','Ильинична'
    );

    v_city t_city := t_city(
        'Москва','Санкт-Петербург','Новосибирск','Екатеринбург','Казань',
        'Нижний Новгород','Челябинск','Самара','Омск','Ростов-на-Дону',
        'Уфа','Красноярск','Воронеж','Пермь','Волгоград','Краснодар','Саратов'
    );

    v_blood t_blood := t_blood('I+','I-','II+','II-','III+','III-','IV+','IV-');

    v_diag t_diag := t_diag(
        'Гипертоническая болезнь II стадии','Сахарный диабет 2 типа','Хронический гастрит',
        'Остеохондроз поясничного отдела','Бронхиальная астма','Ишемическая болезнь сердца',
        'Варикозная болезнь вен нижних конечностей','Хронический пиелонефрит',
        'Хронический бронхит','Язвенная болезнь желудка','Мигрень','Артериальная гипотензия',
        'Хронический тонзиллит','Атеросклероз','Нейроциркуляторная дистония',
        'Артроз коленного сустава','Хронический холецистит','Железодефицитная анемия',
        'Хронический синусит','Ожирение II степени','Здоров','Здоров','Здоров'
    );

    v_gender   VARCHAR2(2 CHAR);
    v_last_n   VARCHAR2(50);
    v_first_n  VARCHAR2(50);
    v_mid_n    VARCHAR2(50);
    v_birth    DATE;
    v_policy   VARCHAR2(20);
    v_passport VARCHAR2(20);
    v_doc_id   NUMBER;
    v_doc_max  NUMBER;

BEGIN
    SELECT MAX(DOCTOR_ID) INTO v_doc_max FROM DOCTORS;

    FOR i IN 1..1500 LOOP
        IF MOD(i, 2) = 0 THEN
            v_gender  := 'М';
            v_last_n  := v_last(MOD(i-1, v_last.COUNT) + 1);
            v_first_n := v_first_m(MOD(i-1, v_first_m.COUNT) + 1);
            v_mid_n   := v_mid_m(MOD(i-1, v_mid_m.COUNT) + 1);
        ELSE
            v_gender  := 'Ж';
            v_last_n  := v_last(MOD(i-1, v_last.COUNT) + 1) || 'а';
            v_first_n := v_first_f(MOD(i-1, v_first_f.COUNT) + 1);
            v_mid_n   := v_mid_f(MOD(i-1, v_mid_f.COUNT) + 1);
        END IF;

        v_birth   := DATE '1940-01-01' + MOD(i * 179, 26645); -- возраст 5-86 лет (1940-2025)
        v_policy  := LPAD(TO_CHAR(i * 7919 + 1000000000), 16, '0');
        v_passport:= TO_CHAR(MOD(i * 31, 90) + 10) || TO_CHAR(MOD(i * 17, 90) + 10) ||
                     ' ' || LPAD(TO_CHAR(MOD(i * 13337, 999999)), 6, '0');
        v_doc_id  := MOD(i * 7, v_doc_max) + 1;

        INSERT INTO PATIENTS (
            PATIENT_ID, LAST_NAME, FIRST_NAME, MIDDLE_NAME,
            BIRTH_DATE, GENDER, PASSPORT, POLICY_NUMBER, BLOOD_TYPE,
            PHONE, ADDRESS, CITY, REGISTRATION_DATE,
            PRIMARY_DOCTOR_ID, DIAGNOSIS, ALLERGIES, IS_ACTIVE
        ) VALUES (
            SEQ_PATIENTS.NEXTVAL,
            v_last_n,
            v_first_n,
            v_mid_n,
            v_birth,
            v_gender,
            v_passport,
            v_policy,
            v_blood(MOD(i-1, v_blood.COUNT) + 1),
            '+7-9' || TO_CHAR(MOD(i * 43, 90) + 10) || '-' ||
                TO_CHAR(MOD(i * 97, 900) + 100) || '-' ||
                TO_CHAR(MOD(i * 151, 90) + 10) || '-' ||
                TO_CHAR(MOD(i * 23, 90) + 10),
            'ул. ' || v_last_n || ', д.' || TO_CHAR(MOD(i, 200) + 1) ||
                ', кв.' || TO_CHAR(MOD(i * 3, 300) + 1),
            v_city(MOD(i-1, v_city.COUNT) + 1),
            SYSDATE - MOD(i * 11, 1825),
            v_doc_id,
            v_diag(MOD(i-1, v_diag.COUNT) + 1),
            CASE MOD(i, 5)
                WHEN 0 THEN 'Пенициллин'
                WHEN 1 THEN 'Аспирин'
                WHEN 2 THEN 'Нет'
                WHEN 3 THEN 'Сульфаниламиды'
                ELSE NULL
            END,
            CASE WHEN MOD(i, 25) = 0 THEN 'N' ELSE 'Y' END
        );

        IF MOD(i, 100) = 0 THEN
            COMMIT;
        END IF;
    END LOOP;

    COMMIT;
    DBMS_OUTPUT.PUT_LINE('Пациенты вставлены: 1500 строк');
END;
