    v_pat_max  NUMBER;
    v_doc_max  NUMBER;
    v_appt_date DATE;
    v_duration NUMBER;

BEGIN
    SELECT MAX(PATIENT_ID) INTO v_pat_max FROM PATIENTS;
    SELECT MAX(DOCTOR_ID)  INTO v_doc_max FROM DOCTORS;

    FOR i IN 1..1000 LOOP
        v_pat_id    := MOD(i * 11, v_pat_max) + 1;
        v_doc_id    := MOD(i * 7,  v_doc_max) + 1;
        v_appt_date := SYSDATE - MOD(i * 13, 730); -- последние 2 года
        v_duration  := CASE MOD(i, 4)
                           WHEN 0 THEN 15
                           WHEN 1 THEN 30
                           WHEN 2 THEN 45
                           ELSE 60
                       END;

        INSERT INTO APPOINTMENTS (
            APPOINTMENT_ID, PATIENT_ID, DOCTOR_ID,
            APPOINTMENT_DATE, APPOINTMENT_TIME, DURATION_MIN,
            STATUS, VISIT_TYPE, COMPLAINTS, DIAGNOSIS, RECOMMENDATIONS, CREATED_DATE
        ) VALUES (
            SEQ_APPOINTMENTS.NEXTVAL,
            v_pat_id,
            v_doc_id,
            v_appt_date,
            v_time(MOD(i-1, v_time.COUNT) + 1),
            v_duration,
            v_status(MOD(i-1, v_status.COUNT) + 1),
            v_type(MOD(i-1, v_type.COUNT) + 1),
            v_complaint(MOD(i-1, v_complaint.COUNT) + 1),
            CASE WHEN v_status(MOD(i-1, v_status.COUNT) + 1) IN ('Завершён')
                 THEN v_diag(MOD(i-1, v_diag.COUNT) + 1)
                 ELSE NULL
            END,
            CASE WHEN v_status(MOD(i-1, v_status.COUNT) + 1) IN ('Завершён')
                 THEN v_rec(MOD(i-1, v_rec.COUNT) + 1)
                 ELSE NULL
            END,
            v_appt_date - MOD(i, 7)
        );

        IF MOD(i, 100) = 0 THEN
            COMMIT;
        END IF;
    END LOOP;

    COMMIT;
    DBMS_OUTPUT.PUT_LINE('Приёмы вставлены: 1000 строк');
END;
